

 public class Cuenta {
	private String email;
	private String contrasena;

   public Cuenta(String email, String contrasena) {
	   this.email = email;
	   this.contrasena = email;
   }

   public String getEmail() {
	   return this.email;
   }
   
   public String getContrasena() {
	   return this.contrasena;
   }
   
   

}